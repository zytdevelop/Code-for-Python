def starbar(num):
    """ 打印 * 分割线
    
    :arg num: 线长
    """
    print('*' * num)
    return None

def hashbar(num):
    """ 打印 # 分割线
    
    :arg num: 线长
    """
    print('#' * num)
    return None
    
def simplestar(num):
    """ 打印 - 分割线
    
    : arg num : 线长
    """
    print('-' * num)
    return None
    
 
